<h2>Focusing on Servers to Deliver the Cloud Through a Converged Infrastructure</h2>

<p>Many savvy IT organizations are recognizing that converged infrastructure is a critical enabling
technology for <a href="/document/1337715724_610"><b>successful cloud computing
deployments</b></a>. In a converged cloud the infrastructures for development and operations are
consistently visible, accessible and manageable. Standardization allows portability of services
across deployment models and IT elements are shared and pooled for quick assembly and disassembly
to support advanced agility and user flexibility.</p>

<div class="micrositeContentCallout">
<div class="micrositeContentCalloutInner">
<h6>Related</h6>

<ul>
<li><a href="/document/1337715724_610">Delivering an Integrated Infrastructure for the Cloud
(IDC)</a></li>

<li><a href="/document/1343143410_165">Build and manage your infrastructure as a service - HP
CloudSystem Matrix</a></li>

<li><a href="/document/1343143381_620">IDC Business Value of Blade</a></li>
</ul>
</div>
</div>

<p>In cloud environments your servers must be able to support the highest levels of virtualization,
whether they are rack, <a href="/document/1343143381_620"><b>blade</b></a>, tower or scalable
systems. It is important to make sure that servers supporting your cloud deployments are the most
up-to-date and highest performing machines in your infrastructure. You also may want to consider a
faster refresh cycle for the cloud.</p>
<p>Why such a critical focus on new servers in your converged infrastructure? Because today’s HP
ProLiant Gen8 servers are designed to address <a href="/document/1343143381_302"><b>data center
requirements in the cloud era</b></a>, featuring embedded automation and intelligent management
features that change the paradigm for cloud performance and manageability. Key innovations achieved
in Gen8 servers are simply not available in earlier generations. With the intelligence built into
today’s ProLiant servers through the HP ProActive Insight Architecture, IT can eliminate tedious
tasks while dramatically boosting application performance through automation and optimization.</p>

<p>It is also important to have a converged infrastructure that integrates with your existing
environment and can manage both physical and virtual machines. Solutions such as the HP CloudSystem
Matrix greatly <a href="/document/1343143410_165"><b>reduce complexity for the cloud</b></a> by
combining hardware, storage, software and services into one solution. Read on to learn why focusing
on server hardware is critical in deploying the cloud through a converged infrastructure.</p>
